    private void showsubMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_showsubMouseClicked
        if( Sclass.getSelectedIndex() == 0 ){
            JOptionPane.showMessageDialog(null,"Please Select Class","Message",2);
        }else{
            try{
                conn = DatabaseConnection.ConnectDB();
                String q = "Select Class_Id from Class where Class_Name = '"+Sclass.getSelectedItem().toString()+"'";
                pst = conn.prepareStatement(q);
                rs = pst.executeQuery();
                int i = Sclass.getSelectedIndex();
                if( i == 9 || i == 12 ){
                    
                    String q2 = "select Subject_Code,Subject_Name\n" +
                                "from DeptSubject dp inner join Subject sb "
                                + "on dp.Subject_Id=sb.Subject_Id where Dept_Id=1";
                    PreparedStatement pst2 = conn.prepareStatement(q2);
                    ResultSet rs2 = pst2.executeQuery();
                    Tsubject.setModel(DbUtils.resultSetToTableModel(rs2));
                }else if( i == 10 || i == 13 ){
                    String q2 = "select Subject_Code,Subject_Name\n" +
                                "from DeptSubject dp inner join Subject sb "
                                + "on dp.Subject_Id=sb.Subject_Id where Dept_Id=3";
                    PreparedStatement pst2 = conn.prepareStatement(q2);
                    ResultSet rs2 = pst2.executeQuery();
                    Tsubject.setModel(DbUtils.resultSetToTableModel(rs2));
                }else if( i == 11 || i == 14 ){
                    String q2 = "select Subject_Code,Subject_Name\n" +
                                "from DeptSubject dp inner join Subject sb "
                                + "on dp.Subject_Id=sb.Subject_Id where Dept_Id=2";
                    PreparedStatement pst2 = conn.prepareStatement(q2);
                    ResultSet rs2 = pst2.executeQuery();
                    Tsubject.setModel(DbUtils.resultSetToTableModel(rs2));
                }else{
                    if( rs.next() ){
                        String q2 = "select Subject_Code,Subject_Name \n" +
                                "from SubjectGenarate sg inner join Subject sb "
                                + "on sg.Subject_Id = sb.Subject_Id\n" +
                                "where Class_Id = "+rs.getInt("Class_Id")+"";
                        PreparedStatement pst2 = conn.prepareStatement(q2);
                        ResultSet rs2 = pst2.executeQuery();
                        Tsubject.setModel(DbUtils.resultSetToTableModel(rs2));
                    }
                }
            
            }catch(Exception e ) {JOptionPane.showMessageDialog(null, e);}
        }
    }//GEN-LAST:event_showsubMouseClicked

