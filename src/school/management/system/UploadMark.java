    private void SubmitMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_SubmitMouseClicked
        if( id.getText().isEmpty() || exam.getSelectedIndex() == 0 || year.getSelectedIndex() == 0 || mark.getText().isEmpty() || subcode.getText().isEmpty() ){
            JOptionPane.showMessageDialog(null,"Fill Up Fields Properly","Message",2);
        }else{
            try{
                conn = DatabaseConnection.ConnectDB();
                String q1 = "Select Max(Class_Id) as Class_Id from StudentAccademicInfo where Student_Id = '"+id.getText()+"'";
                pst = conn.prepareStatement(q1);
                rs = pst.executeQuery();
                if( rs.next() ){
                    int clsid = Integer.parseInt( rs.getString("Class_Id"));
                    
                    String q2 = "select Exam_Id from Examination where Exam_Name = '"+exam.getSelectedItem().toString()+"'";
                    PreparedStatement pst2 = conn.prepareStatement(q2);
                    ResultSet rs2 = pst2.executeQuery();
                    int examid = 0 ;
                    if( rs2.next() ){
                        examid = Integer.parseInt( rs2.getString("Exam_Id"));
                    }
                    String q3="select Subject_Id from Subject where Subject_Code = '"+subcode.getText()+"'";
                    PreparedStatement pst3 = conn.prepareStatement(q3);
                    ResultSet rs3 = pst3.executeQuery();
                    int sbid  = 0 ;
                    if( rs3.next() ){
                        sbid = Integer.parseInt( rs3.getString("Subject_Id"));
                    }
                    String q4 = "select Year_Id from Year where Year = "+year.getSelectedItem().toString()+"";
                    PreparedStatement pst4 = conn.prepareStatement(q4);
                    ResultSet rs4 = pst4.executeQuery();
                    int yid =  0 ;
                    if( rs4.next() ){
                        yid = Integer.parseInt( rs4.getString("Year_Id"));
                    }
                    
                    int Grade_Id = 0 , obmk = Integer.parseInt(mark.getText());
                    if( obmk >= 80 && obmk <=100 ){Grade_Id =1 ;}else if( obmk >= 70 && obmk <80 ){Grade_Id = 2;}
                    else if( obmk >= 60 && obmk < 70 ){Grade_Id = 3;}else if( obmk >= 50 && obmk < 60 ){Grade_Id = 4;}
                    else if( obmk >= 40 && obmk < 50 ){Grade_Id = 5;}else if( obmk >= 33 && obmk < 40 ){Grade_Id = 6;}
                    else if( obmk >= 0 && obmk < 33 ){Grade_Id = 7;}
                    
                    String a = "select Subject_Code from Subject where Subject_Code=?";
                    PreparedStatement pst12 = conn.prepareStatement(a);
                    pst12.setString(1, subcode.getText());
                    ResultSet rs10 = pst12.executeQuery();
                    if( rs10.next() ){
                        try{
                            String q5 = "insert into Marks values("+clsid+","+examid+",'"+id.getText()+"',"+sbid+","+obmk+","+Grade_Id+","+yid+")";
                            PreparedStatement pst5 = conn.prepareStatement(q5);
                            pst5.execute();
                            subcode.setText("");mark.setText("");
                            JOptionPane.showMessageDialog(null,"Marks Succesfully Inserted\nClick Refresh to Check");
                        }catch(Exception e){
                            JOptionPane.showMessageDialog(null,"Mark Already Updated");
                        }
                    }else{JOptionPane.showMessageDialog(null,"Subject Code Not Found!!");}
                     
                }
                
            }catch(Exception e ) {JOptionPane.showMessageDialog(null, e);}
        }
 
    }//GEN-LAST:event_SubmitMouseClicked

