    private void searchActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_searchActionPerformed
        try{ 
            connection = DatabaseConnection.ConnectDB();
            String query = "select Student_Id,MAX(Class_Id) as Class_Id,Section_Id,Depertment_Name,"+
                "MAX(Year_Id) as Year_Id\n" +
                "from StudentAccademicInfo where Student_Id = '"+id.getText()+"'\n" +
                "group by Student_Id , Section_Id,Depertment_Name";
            pst = connection.prepareStatement(query);
            rs = pst.executeQuery();
            if( rs.next()){
                studentid.setText(rs.getString("Student_Id"));
                dept.setText(rs.getString("Depertment_Name"));
                
                /* Getting Class Name from Class Table */ 
                String qu = "select Class_Name from Class where Class_Id = "+rs.getInt("Class_Id")+"";
                PreparedStatement ps = connection.prepareStatement(qu);
                ResultSet rset = ps.executeQuery();
                if( rset.next() ){
                    sclass.setText(rset.getString("Class_Name"));
                }
                ps.close();rset.close();
                
                /* Getting Section Name from Section Table */ 
                qu = "select Section_Name from Section where Section_Id = "+rs.getInt("Section_Id")+"";
                ps = connection.prepareStatement(qu);
                rset = ps.executeQuery();
                if( rset.next() ){
                    section.setText(rset.getString("Section_Name"));
                }
                ps.close();rset.close();
                
                /* Getting Year Name from Year Table */ 
                qu = "select Year from Year where Year_Id = "+rs.getInt("Year_Id")+"";
                ps = connection.prepareStatement(qu);
                rset = ps.executeQuery();
                if( rset.next() ){
                    year.setText(rset.getString("Year"));
                }
                ps.close();rset.close();
                
                ResultSet rs2 ;
                String query2 = "select * from StudentPersonalInfo where Student_Id = '"+id.getText()+"'";
                PreparedStatement pst2 = connection.prepareStatement(query2);
                rs2 = pst2.executeQuery();
                if(rs2.next()){
                    name.setText(rs2.getString("Student_Name"));
                    fname.setText(rs2.getString("Fathers_Name"));
                    fphn.setText(rs2.getString("Father_Phone_Number"));
                    mname.setText(rs2.getString("Mothers_Name"));
                    mphn.setText(rs2.getString("Mother_Phone_Number"));
                    dob.setText(rs2.getString("Date_Of_Birth"));
                    age.setText(rs2.getString("Age"));
                    rel.setText(rs2.getString("Religion"));
                    sex.setText(rs2.getString("Sex"));
                    doa.setText(rs2.getString("Admission_Date"));
                    nation.setText(rs2.getString("Nationality"));
                    
                }
                
                ResultSet rs3 ;
                String query3 = "select * from StudentPresentAddress where Student_Id = '"+id.getText()+"'";
                PreparedStatement pst3 = connection.prepareStatement(query3);
                rs3 = pst3.executeQuery();
                if(rs3.next()){
                    pre_house.setText(rs3.getString("HouseNo_Road"));
                    pre_village.setText(rs3.getString("Village"));
                    pre_posto.setText(rs3.getString("PostOffice"));
                    pre_postcode.setText(rs3.getString("PostalCode"));
                    pre_district.setText(rs3.getString("District"));
                    pre_subdistrict.setText(rs3.getString("SubDistrict"));
                    pre_division.setText(rs3.getString("Division"));
                    pre_country.setText(rs3.getString("Country"));  
                }
                
                ResultSet rs4 ;
                String query4 = "select * from StudentParmanentAddress where Student_Id = '"+id.getText()+"'";
                PreparedStatement pst4 = connection.prepareStatement(query4);
                rs4 = pst4.executeQuery();
                if(rs4.next()){
                    par_house.setText(rs4.getString("HouseNo_Road"));
                    par_village.setText(rs4.getString("Village"));
                    par_posto.setText(rs4.getString("PostOffice"));
                    par_postcode.setText(rs4.getString("PostalCode"));
                    par_district.setText(rs4.getString("District"));
                    par_subdistrict.setText(rs4.getString("SubDistrict"));
                    par_division.setText(rs4.getString("Division"));
                    par_country.setText(rs4.getString("Country"));  
                }
            }
            else{JOptionPane.showMessageDialog(null,"No Student Found!","Error",2 );}
        }
        catch(Exception e){JOptionPane.showMessageDialog(null, e );}
    }//GEN-LAST:event_searchActionPerformed

