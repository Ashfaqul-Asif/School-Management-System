    private void refreshActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_refreshActionPerformed
        try{
            conn = DatabaseConnection.ConnectDB();
            int wa = whad.getSelectedIndex();
            if( wa == 0 ){
                /* Set teacher & Worker field not editable */
                speciality.setEditable(true);
                twid.setEditable(false);
                twname.setEditable(false);
                scs = studentclass.getSelectedIndex();
                if( scs == 9 ){
                    dept.setEnabled(true);
                }
                else{dept.setEnabled(false);}
                qualification.setEditable(false);
                extraor.setEditable(false);
                studentid.setEditable(true);
                sname.setEditable(true);
                studentclass.setEnabled(true);
                section.setEnabled(true);
                designation.setEnabled(false);
                twid.setText("");

                studentid.setText("ST-");
                String query = "select Student_Id from StudentPersonalInfo where SL_No=(select MAX(SL_No) as Student_Id from StudentPersonalInfo);";
                pst = conn.prepareStatement(query);
                rs = pst.executeQuery();
                if(rs.next()){
                    lastadmitted.setText(rs.getString("Student_Id"));
                }

                int sc = studentclass.getSelectedIndex();
                int sec = section.getSelectedIndex();
                String q = "select MAX(Class_Roll)+1 as clsroll from StudentAccademicInfo where Class_Id = "+sc+" and Section_Id = "+sec+"";
                pst = conn .prepareStatement(q);
                rs = pst.executeQuery();
                if( rs.next() ){
                    classroll.setText(rs.getString("clsroll"));    
                }
                if(classroll.getText().equals("")){
                    classroll.setText("1");
                }
            }else if( wa == 1 ){
                // set Student's part not editable
                speciality.setEditable(false);
                studentid.setEditable(false);
                sname.setEditable(false);
                studentclass.setEnabled(false);
                section.setEnabled(false);
                dept.setEnabled(false);
                twid.setEditable(true);
                twname.setEditable(true);
                designation.setEnabled(true);
                qualification.setEditable(true);
                extraor.setEditable(true);
                studentid.setText("");
                
                twid.setText("T-");
                String q = "select Teacher_Id from TeacherAccademicInfo where Sl_No=(select MAX(SL_No) as Teacher_Id from TeacherAccademicInfo);";
                pst = conn.prepareStatement(q);
                rs = pst.executeQuery();
                if( rs.next() ){
                    lastadmitted.setText(rs.getString("Teacher_Id"));
                }
                classroll.setText("");
            }
            else if( wa == 2 ){
                // set Student's part not editable
                speciality.setEditable(false);
                studentid.setEditable(false);
                sname.setEditable(false);
                studentclass.setEnabled(false);
                section.setEnabled(false);
                dept.setEnabled(false);
                twid.setEditable(true);
                twname.setEditable(true);
                designation.setEnabled(true);
                qualification.setEditable(false);
                extraor.setEditable(true);
                studentid.setText("");
                twid.setText("W-");
                String q = "select Worker_Id from WorkerAccademicInfo where Sl_No=(select MAX(SL_No) as Worker_Id from WorkerAccademicInfo);";
                pst = conn.prepareStatement(q);
                rs = pst.executeQuery();
                if( rs.next() ){
                    lastadmitted.setText(rs.getString("Worker_Id"));
                }
                classroll.setText("");
            }
            
        }catch(Exception c ){JOptionPane.showMessageDialog(null, "Nothing Found");}
    }//GEN-LAST:event_refreshActionPerformed

