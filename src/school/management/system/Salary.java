    private void showMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_showMouseClicked
        int s = tw.getSelectedIndex();
        if( s == 0 ){
            JOptionPane.showMessageDialog(null,"Please select a option first");
        }
        else if( s == 1 ){
            try{
                Connection conn = DatabaseConnection.ConnectDB();
                String sql = "select tai.Teacher_Id , Teacher_Name , tpi.Qualification , Rank_Name \n" +
                    "as Designation ,Pay_Of_Ammount as Salary , tai.DateOfJoin as [Date of Join]\n" +
                    "from TeacherAccademicInfo tai inner join TeacherPersonalInfo tpi on tai.Teacher_Id = tpi.Teacher_Id\n" +
                    "inner join Designation dg on tai.Rank_Id = dg.Rank_Id\n" +
                    "inner join TeacherSalary ts on tai.Teacher_Id = ts.Teacher_Id";
                PreparedStatement pst = conn.prepareStatement(sql);
                ResultSet rs = pst.executeQuery();
                Tsalary.setModel(DbUtils.resultSetToTableModel(rs));
            }catch(Exception e ){JOptionPane.showMessageDialog(null, e);}
        }
        else if( s == 2 ){
            try{
                Connection conn = DatabaseConnection.ConnectDB();
                String sql = "select wai.Worker_Id , Worker_Name ,  Rank_Name \n" +
                    "as Designation ,Pay_Of_Ammount as Salary , wai.DateOfJoin as [Date of Join]\n" +
                    "from workerAccademicInfo wai inner join WorkerPersonalInfo wpi on wai.Worker_Id = wpi.Worker_Id\n" +
                    "inner join Designation dg on wai.Rank_Id = dg.Rank_Id\n" +
                    "inner join workerSalary ws on wai.Worker_Id = ws.Worker_Id";
                PreparedStatement pst = conn.prepareStatement(sql);
                ResultSet rs = pst.executeQuery();
                Tsalary.setModel(DbUtils.resultSetToTableModel(rs));
            }catch(Exception e ){JOptionPane.showMessageDialog(null, e);}
        }
    }//GEN-LAST:event_showMouseClicked

