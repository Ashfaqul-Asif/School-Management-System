    private void refresh(){
        try{
            conn = DatabaseConnection.ConnectDB();
            String query = "select Count( distinct Student_Id) as Total_Student from StudentAccademicInfo";
            String query2 = "select Count(Teacher_Id) as Total_Teacher from TeacherPersonalInfo";
            String query3 = "select Count(Worker_Id) as Total_Others from WorkerPersonalInfo";
            pst = conn.prepareStatement(query);
            PreparedStatement pst2 = conn.prepareStatement(query2);
            PreparedStatement pst3 = conn.prepareStatement(query3);
            rs = pst.executeQuery();
            ResultSet rs2 = pst2.executeQuery();
            ResultSet rs3 = pst3.executeQuery();
            if( rs.next() ){
                totalstudents.setText( rs.getString("Total_Student"));
            }
            if( rs2.next() ){
                totalteachers.setText( rs2.getString("Total_Teacher"));
            }
            if( rs3.next() ){
                totalother.setText( rs3.getString("Total_Others"));
            }
        }catch( ClassNotFoundException | SQLException e ){JOptionPane.showMessageDialog(null, e);}
    }
